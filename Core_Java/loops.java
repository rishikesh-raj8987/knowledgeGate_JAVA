package Core_Java;

public class loops {
    public static void main (String [] args){
        //  while loop
        int i=1;
        while(i<=9){
            System.out.println(i);
            i++; // i= i+1;
        }
        System.out.println("Done");

        //for loop
        for(int j=1;j<=9;j++){
            System.out.println(j);
        }
        System.out.println("For loop is done");
    }
}
